import streamlit as st

st.header("Hvad er Connect 4?")

st.write("""
Connect 4 er et klassisk og taktisk brætspil for to spillere. Målet er at få fire af sine egne brikker på stribe – enten vandret, lodret eller diagonalt – før modstanderen gør det. 

#### Spillets opbygning
Spillet foregår på et lodret bræt med 6 rækker og 7 kolonner. Spillerne skiftes til at slippe én farvet brik (typisk rød eller gul) ned i en valgfri kolonne. Brikken falder automatisk ned til den nederste ledige plads i kolonnen.

#### Taktik og strategi
Det kræver både strategi og taktisk sans at få fire på stribe. Spillerne skal både fokusere på at danne deres egen række og samtidig blokere modstanderens forsøg på at gøre det samme.

#### Hvorfor er Connect 4 så populært?
Connect 4 er let at lære, men byder på dybde og udfordringer, der gør det spændende for både børn og voksne. Spillet styrker logisk tænkning, planlægning og taktisk forståelse og har derfor været en elsket klassiker i generationer.
""")
